hr

echo -ne "\n   ${C_WHITE}● Checking Python 3 ...${C_RESET} "
if ! command -v python3 >/dev/null 2>&1; then
    echo -e "${C_RED}NOT FOUND${C_RESET}"
    echo -e "   ${C_RED}Python 3 is required. Please install it and try again.${C_RESET}"
    exit 1
fi
